SELECT
  P.CLIENTE_ID,
  MIN(P.DATA_PEDIDO) AS DATA_PRIMEIRO_PEDIDO,
  MAX(P.DATA_PEDIDO) AS DATA_ULTIMO_PEDIDO,
  COUNT(P.PEDIDO_ID) AS QTD_TOTAL_PEDIDOS,
  ROUND(SUM(PV.VALOR_TOTAL), 2) AS VALOR_TOTAL_PEDIDOS,
  ROUND(AVG(PV.VALOR_TOTAL), 2) AS VALOR_MEDIO_PEDIDOS,
  DATE_DIFF(CURRENT_DATE(), MIN(P.DATA_PEDIDO), DAY) AS DIAS_DESDE_PRIMEIRA_COMPRA,
  DATE_DIFF(CURRENT_DATE(), MAX(P.DATA_PEDIDO), DAY) AS DIAS_DESDE_ULTIMA_COMPRA
FROM {{ ref('stg_pedidos') }} AS P
JOIN {{ ref('int_pedido_valores') }} AS PV
  ON P.PEDIDO_ID = PV.PEDIDO_ID
GROUP BY P.CLIENTE_ID