SELECT
  IP.ITEM_ID,
  IP.PEDIDO_ID,
  PE.CLIENTE_ID,
  PE.DATA_PEDIDO,
  PE.FORMA_PAGAMENTO,
  PE.STATUS,
  PE.CANAL,
  PE.ORIGEM,
  PE.TRANSPORTADORA,
  IP.PRODUTO_ID,
  PR.PRODUTO,
  PR.CATEGORIA,
  PR.MARCA,
  IP.QUANTIDADE,
  IP.VALOR_UNITARIO,
  IP.SUBTOTAL
FROM {{ ref('stg_itens_pedido') }} AS IP
JOIN {{ ref('stg_pedidos') }} AS PE
  ON IP.PEDIDO_ID = PE.PEDIDO_ID
JOIN {{ ref('stg_produtos') }} AS PR
  ON IP.PRODUTO_ID = PR.PRODUTO_ID
